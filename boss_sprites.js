function getBossSprite(design) {
  return BOSS_SPRITES[design] || BOSS_SPRITES.fortress;
}

export function getBossMainColor(design) {
  return getBossSprite(design).palette[1];
}

function drawPixelSprite(ctx, sprite, palette, x, y, width, height, frameCount = 0, animationSpeed = 1) {
  const frameIndex = Math.floor((frameCount * animationSpeed) / sprite.frameDuration) % sprite.frames.length;
  const frame = sprite.frames[frameIndex];
  const rows = frame.length;
  const cols = frame[0].length;
  const cellW = width / cols;
  const cellH = height / rows;

  for (let r = 0; r < rows; r++) {
    const row = frame[r];
    for (let c = 0; c < cols; c++) {
      const key = row[c];
      if (key === '0') continue;
      ctx.fillStyle = palette[key] || palette[1];
      ctx.fillRect(x + c * cellW, y + r * cellH, Math.ceil(cellW), Math.ceil(cellH));
    }
  }
}

export function drawBoss(ctx, enemy, x, y, frameCount = 0, animationSpeed = 1) {
  const sprite = getBossSprite(enemy.design);
  const palette = sprite.palette;

  ctx.save();
  ctx.shadowBlur = 6;
  ctx.shadowColor = palette[7] || palette[1];
  drawPixelSprite(ctx, sprite, palette, x, y, enemy.width, enemy.height, frameCount, animationSpeed);
  drawBossHp(ctx, enemy, x, y);
  ctx.restore();
}

function drawBossHp(ctx, enemy, x, y) {
  const hpRatio = Math.max(0, enemy.hp / enemy.maxHp);
  ctx.fillStyle = 'rgba(255, 0, 0, 0.5)';
  ctx.fillRect(x, y - 10, enemy.width, 6);
  ctx.fillStyle = '#00ff88';
  ctx.fillRect(x, y - 10, enemy.width * hpRatio, 6);
}
